# For loop:
# For loop can execute a block of code multiple time.
#
# Syntax of for loop:
# For variable name in the range (start, stop, step), code has to be executed multiple times.

#Example:
for i in range(1, 10):
    print(i)

x = list(range(1, 10))
print(x)


#Example:
for i in range(1, 10, 1):
    print(i)

#Example:
for i in range(1, 10, 2):
    print(i)

#Example:
for i in range(1, 12, 1):
    print("Hello", i)